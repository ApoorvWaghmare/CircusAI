from django.db import models
from django.db.models import CharField, DateTimeField, ForeignKey
from django.utils.translation import gettext_lazy as _

#----------#

from apps.post.models.mysql_models import Post
from apps.user.models.mysql_models import User

#======================================================================================================================#
# CommentManager
#======================================================================================================================#

class CommentManager(models.Manager):
    
    #------------------------------------------------------------------------------------------------------------------#

    def create_user(self):
        pass

#======================================================================================================================#
# End of CommentManager
#======================================================================================================================#

#======================================================================================================================#
# Comment
#======================================================================================================================#

class Comment(models.Model):
    
    #------------------------------------------------------------------------------------------------------------------#

    class Meta:
        db_table = 'comment'

    #------------------------------------------------------------------------------------------------------------------#

    objects = CommentManager()

    #------------------------------------------------------------------------------------------------------------------#
    
    post_id = ForeignKey(Post, on_delete = models.CASCADE, null = False, related_name = 'post_comment')
    user_id = ForeignKey(User, on_delete = models.CASCADE, null = False, related_name = 'user_comment')
    timestamp = DateTimeField(auto_now_add = True)
    comment_ref = CharField(max_length = 255, null = True)

    POST_ID = 'post_id'
    USER_ID = 'user_id'
    COMMENT_REF = 'comment_ref'

#======================================================================================================================#
# End of Comment
