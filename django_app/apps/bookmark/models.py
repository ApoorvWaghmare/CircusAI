#======================================================================================================================#

class Bookmark(models.Model):
    
    #------------------------------------------------------------------------------------------------------------------#

    class Meta:
        db_table = 'bookmark'

    #------------------------------------------------------------------------------------------------------------------#

    objects = BookmarkManager()

    #------------------------------------------------------------------------------------------------------------------#

    user_id = ForeignKey(User, on_delete = models.CASCADE, null = False, related_name = 'user_bookmark')
    post_id = ForeignKey(Post, on_delete = models.CASCADE, null = False, related_name = 'post_bookmark')

    USER_ID = 'user_id'
    POST_ID = 'post_id'

