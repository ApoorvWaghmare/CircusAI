from rest_framework.serializers import ModelSerializer

#----------#

from .models import Engagement

#======================================================================================================================#
# EngagementSerializer
#======================================================================================================================#

class EngagementSerializer(ModelSerializer):
    class Meta:
        model = Engagement
        fields = ["post_id",
                  "timestamp",
                  "engagement_type"]

#======================================================================================================================#
# End of EngagementSerializer
#======================================================================================================================#

#======================================================================================================================#
# DeleteEngagementSerializer
#======================================================================================================================#

class DeleteEngagementSerializer(ModelSerializer):
    class Meta:
        model = Engagement
        fields = ["post_id"]

#======================================================================================================================#
# End of DeleteEngagementSerializer
#======================================================================================================================#
